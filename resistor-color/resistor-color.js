export const colorCode = (color) => {
  color = color.toLowerCase();
  return COLORS.indexOf(color);
};

export const COLORS = [
  'black',
  'brown',
  'red',
  'orange',
  'yellow',
  'green',
  'blue',
  'violet',
  'grey',
  'white',
];
